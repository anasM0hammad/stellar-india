  var temp = count - x ;
  var temp2 = clients - 30 ;
  var temp3 = projects - 30 ;

 var counting = ()=>{
	if(temp <= count){
		setTimeout(()=>{
			document.getElementById("visitors").innerText = temp ;
			temp += 11 ;
			counting() ;
		},50) ;
	}
	else{
		setTimeout(() => {
			document.getElementById("visitors").innerText = count ;
		}, 50);
	}
 };


